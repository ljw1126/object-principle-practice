package org.eternity.adventure.vo;

import org.eternity.adventure.constant.Direction;

public class Position {
    private final int x;
    private final int y;

    private Position(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public static Position of(int x, int y) {
        return new Position(x, y);
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public Position shift(Direction direction) {
        return switch(direction) {
            case NORTH -> Position.of(x, y - 1);
            case EAST -> Position.of(x + 1, y);
            case SOUTH -> Position.of(x, y + 1);
            case WEST -> Position.of(x - 1, y);
        };
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + x;
        result = prime * result + y;
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        Position other = (Position) obj;
        if (x != other.x)
            return false;
        if (y != other.y)
            return false;
        return true;
    }

    
}
